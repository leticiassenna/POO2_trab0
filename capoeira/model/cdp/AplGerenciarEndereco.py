class AplGerenciarEndereco():

    def cadastrar(endereco):
        endereco_novo = endereco
        endereco_novo.logradouro = endereco.logradouro
        endereco_novo.numero = endereco.numero
        endereco_novo.bairro = endereco.bairro
        endereco_novo.cidade = endereco.cidade
        endereco_novo.complemento = endereco.complemento




